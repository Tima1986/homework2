var size = parseInt(prompt('Укажите размер(СНГ)'))
if (size === 40) {
    alert('Американский размер S')
}
else if (size === 42) {
    alert('Американский размер M')
}
else if (size === 46) {
    alert('Американский размер L')
}
else if (size === 50) {
    alert('Американский размер XL')
}
else if (size === 54) {
    alert('Американский размер XXL')
}
